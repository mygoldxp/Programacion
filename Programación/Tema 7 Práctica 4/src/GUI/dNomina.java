    private void bConfeccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bConfeccionarActionPerformed
            // TODO add your handling code here:
        try{
            if(bConfeccionar.getText().equals("Calcular")){
                calcular();
                bConfeccionar.setText("Continuar");
            }
            else{
                switch(this.getTitle()){
                    case "Confeccionar Nómina":
                        Main.agregarNomina(0);
                        Main.reabrir(2,this.getTitle(), this);
                        break;
                        
                    case "Modificar Nómina":
                        Main.eliminarNomina(nomina);
                        Main.agregarNomina(1);
                        Main.reabrir(2,this.getTitle(), this);
                        break;
                        
                    case "Eliminar Nómina":
                        eliminar();
                        break;
                        
                }
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getClass());
        }
    }//GEN-LAST:event_bConfeccionarActionPerformed

