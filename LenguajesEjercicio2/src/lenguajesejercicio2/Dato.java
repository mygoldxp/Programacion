package lenguajesejercicio2;

/**
 *
 * @author v6222
 */
public class Dato {
    
    String hora, dato;

    public Dato() {
    }

    public Dato(String hora, String dato) {
        this.hora = hora;
        this.dato = dato;
    }

    public String getHora() {
        return hora;
    }

    public void setHora(String hora) {
        this.hora = hora;
    }

    public String getDato() {
        return dato;
    }

    public void setDato(String dato) {
        this.dato = dato;
    }
    
    
}
