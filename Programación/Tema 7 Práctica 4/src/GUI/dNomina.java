    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(this.getTitle().equals("Confeccionar Nómina")){
            confeccionar();
        }
        else if(this.getTitle().equals("Modificar Nómina")){
            tExtra.setEditable(true);
            tForzosa.setEditable(true);
            tIncentivo.setEditable(true);
            bConfeccionar.setEnabled(true);
            modificar();
        }
        else{
            modificar();
            bConfeccionar.setText("Eliminar");
            bConfeccionar.setEnabled(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void confeccionar(){
        try{
            empleado = Main.localizar(tEmpleado.getText());
            if(empleado != null){
                tEmpleado.setText(String.valueOf(empleado.getCod()));
                tNombre.setText(empleado.getNombre());
                tCargo.setText(empleado.getCargo().getNombre() + "-" + empleado.getCargo().getCargo());
                tContrato.setText(empleado.getContrato().getContrato());
                tNSS.setText(empleado.getNss());
                double base = empleado.getCargo().getSalarioBase() + empleado.getContrato().getSalarioBase();
                DecimalFormat forma = new DecimalFormat("0.##");
                tBase.setText(String.valueOf(forma.format(base)));
                tExtra.setEditable(true);
                tForzosa.setEditable(true);
                tIncentivo.setEditable(true);
                bConfeccionar.setEnabled(true);
                foto();
            }
            else{
                throw new Error(11);
            }
        }
        catch(Error e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
    private void foto(){
        ImageIcon imagen = new ImageIcon("src/FotosEmpleados/" + tEmpleado.getText() + ".jpg");
        Icon icono = new ImageIcon(imagen.getImage().getScaledInstance(lFoto.getWidth(), lFoto.getHeight(), Image.SCALE_DEFAULT));
        lFoto.setIcon(icono);
        lFoto.repaint();
    }
