    private void bAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAñadirActionPerformed
        // TODO add your handling code here:
        try{
            validar(4, tNombre.getText(), "^[A-Z][a-z]{1,}([ ][A-Z][a-z]{1,})*$", tNombre);
            validar(5, tFecha.getText(), "^[0-9]{1,2}[-][0-9]{1,2}[-][0-9]{4}$", tFecha);
            if(gTipo.getSelection() == null){
                throw new Error(3);
            }
            else{
                if(rActor.isSelected()){
                    Main.agregarPersona(1, tNombre.getText(), tFecha.getText());
                }
                else if(rDirector.isSelected()){
                    Main.agregarPersona(2, tNombre.getText(), tFecha.getText());
                }
                else{
                    Main.agregarPersona(3, tNombre.getText(), tFecha.getText());
                }
                JOptionPane.showMessageDialog(this, "Agregado Correctamente.");
                borrar();
            }
        }
        catch(Error e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getClass());
        }
    }//GEN-LAST:event_bAñadirActionPerformed

    private void bAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAceptarActionPerformed
        // TODO add your handling code here:
        try{
            validar(1, tTitulo.getText(), "^[A-Z][a-z]{1,}([ ][A-Z][a-z]{1,})*$", tTitulo);
            if(cGenero.getSelectedIndex()<0){
                cGenero.setBackground(Color.red);
                cGenero.grabFocus();
                throw new Error(2);
            }
            else{
                cGenero.setBackground(Color.white);
            }
            estadoAlta(Main.agregarTitulo(tTitulo.getText(), cGenero.getSelectedItem().toString()));
            
            borrarTodo();
        }
        catch(Error e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getClass());
        }
    }//GEN-LAST:event_bAceptarActionPerformed

    private void estadoAlta(boolean estado) throws Exception{
        if(estado){
            JOptionPane.showMessageDialog(this, "Alta realizada correctamente.");
        }
        else{
            throw new Error(6);
        }
    }
