    private void tfNombreCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_tfNombreCaretUpdate
        // TODO add your handling code here:
        if(validar(tfNombre.getText(),"^[A-Z][a-z]{2,}([ ][A-Z][a-z]{1,})*$")){
            tfApellidos.setEditable(true);
            tfNombre.setForeground(Color.blue);
        }
        else{
            tfNombre.setForeground(Color.red);
        }
        
    }//GEN-LAST:event_tfNombreCaretUpdate

    private void tfApellidosCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_tfApellidosCaretUpdate
        // TODO add your handling code here:
        if(validar(tfApellidos.getText(),"^[A-Z][a-z]{1,}([ ][A-Z][a-z]{1,})*$")){
            tfDni.setEditable(true);
            tfApellidos.setForeground(Color.blue);
        }
        else{
            tfApellidos.setForeground(Color.red);
        }
    }//GEN-LAST:event_tfApellidosCaretUpdate

    private void tfDniCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_tfDniCaretUpdate
        // TODO add your handling code here:
        
        if(validar(tfDni.getText(),"^[0-9]{8}[A-Za-z]$")){
            bAceptar.setEnabled(true);
            tfDni.setForeground(Color.blue);
            bAceptar.grabFocus();
        }
        else{
            tfDni.setForeground(Color.red);
        }
        
    }//GEN-LAST:event_tfDniCaretUpdate

