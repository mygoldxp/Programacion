    private LocalTime oHora(String hora, String minuto){
        return LocalTime.of(Integer.parseInt(hora), Integer.parseInt(minuto));
    }
    
    private java.sql.Date oFecha(Date fecha){
        SimpleDateFormat formar = new SimpleDateFormat("yyyy-MM-dd");
        String fFecha = formar.format(fecha);
        return java.sql.Date.valueOf(fFecha);
    }
    
    private void aceptarOP1() throws Exception{
        LocalTime horaI, horaF;
        validar(1, tEvento,"^[A-Z][a-z0-9 ]+$");
        validar(2, tLugar,"^[A-Z][a-z0-9 ]+$");
        validar(3, tPlaza,"^[0-9]+$");
        validarFecha(4, cFecha);
        if(Main.consultar(tEvento.getText()) == null){
            horaI = oHora(cIHora.getSelectedItem().toString(), cIMinuto.getSelectedItem().toString());
            horaF = oHora(cFHora.getSelectedItem().toString(), cFMinuto.getSelectedItem().toString());
            Main.añadir(tEvento.getText(), tLugar.getText(), horaI, horaF, oFecha(cFecha.getDate()), Integer.parseInt(tPlaza.getText()));
            JOptionPane.showMessageDialog(this, "Añadido Correctamente.");
            Main.reabrir(this,1);
        }
        else{
            throw new Error(11);
        }
    }
    
