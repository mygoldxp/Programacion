    private void bEmpresaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEmpresaActionPerformed
        try{ 
        validar(12, tCIF,"^[A-Z][0-9]{8}$");
        tCIF.setEnabled(false);
        if(bEmpresa.getText().equals("Comprobar Empresa")){
            Empresa em;
            em = Main.consultarEmpresa(tCIF.getText());
            if(em == null){
                bEmpresa.setText("Añadir Empresa");
                tNombreE.setEnabled(true);
                tTelefonoE.setEnabled(true);
                tDireccionE.setEnabled(true);
            }
            else{
                tNombreE.setText(em.getNombre());
                tDireccionE.setText(em.getDireccion());
                tTelefonoE.setText(em.getTelefono());
                pPersona.setVisible(true);
                this.setSize(555, 525);
            }
        }
        else{
            validar(13, tNombreE,"^[A-Z][a-z. ]+$");
            validar(10, tTelefonoE,"^[6-9][0-9]{8}$");
            validar(8, tDireccionE,"^[A-Z][a-z0-9 ]+$");
            tNombreE.setEnabled(false);
            tTelefonoE.setEnabled(false);
            tDireccionE.setEnabled(false);
            Main.añadirEmpresa(tCIF.getText(), tNombreE.getText(), tTelefonoE.getText(), tDireccionE.getText());
            bEmpresa.setEnabled(false);
            pPersona.setVisible(true);
            this.setSize(555, 520);
        }
        } catch (Error e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        } catch (Exception ee) {
            JOptionPane.showMessageDialog(this, ee.getClass());
        }
    }//GEN-LAST:event_bEmpresaActionPerformed

