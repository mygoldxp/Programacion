    private void bPersonaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bPersonaActionPerformed
        // TODO add your handling code here:
        try{
            validar(7, tDNI, "^[0-9]{8}[A-Z]$");
            tDNI.setEnabled(false);
            
        if(bPersona.getText().equals("Comprobar Persona")){
            if(!Main.consultarPersona(tDNI.getText())){
                bPersona.setText("Añadir Persona");
                enComun(true);
            }
            else{
                Persona p = Main.getP();
                if(tCIF.getText().equals(Main.getEm().getCif())){
                    tNombre.setText(p.getNombre());
                    tApellidos.setText(p.getApellidos());
                    tDireccion.setText(p.getDireccion());
                    tEdad.setText(String.valueOf(p.getEdad()));
                    tTelefono.setText(p.getTelefono());
                    enComun(false);
                    bAceptar.setEnabled(true);
                }
                else{
                    tDNI.setEnabled(true);
                    tDNI.grabFocus();
                    tNombre.setText("");
                    tApellidos.setText("");
                    tDireccion.setText("");
                    tEdad.setText("");
                    tTelefono.setText("");
                    throw new Error(14);
                }
            }
        }
        else{
            validar(5, tNombre, "^[A-Z][a-z ]+$");
            validar(6, tApellidos, "^[A-Z][a-z ]+$");
            validar(8, tDireccion, "^[A-Z][a-z0-9 ]+$");
            validar(9, tEdad, "^[0-9]{1,3}$");
            validar(10, tTelefono, "^[6-9][0-9]{8}$");
            enComun(false);
            Main.añadirPersona( tNombre.getText(), tApellidos.getText(), tDNI.getText(), tDireccion.getText(), 
                    tTelefono.getText(), Integer.parseInt(tEdad.getText()), tCIF.getText());
            bPersona.setEnabled(false);
            bAceptar.setEnabled(true);
        }
        } catch (Error e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        } catch (Exception ee) {
            JOptionPane.showMessageDialog(this, ee.getClass());
        }
        
    }//GEN-LAST:event_bPersonaActionPerformed

    private void cargarDatos(Evento ev){
        tEvento.setEnabled(false);
        tEvento.setText(ev.getNombre());
        tLugar.setText(ev.getLugar());
        tPlaza.setText(String.valueOf(ev.getAforo()));
        cFecha.setDate(ev.getFecha());
        cIHora.setSelectedItem(hora(ev.gethEntrada()));
        cIMinuto.setSelectedItem(minuto(ev.gethEntrada()));
        cFHora.setSelectedItem(hora(ev.gethSalida()));
        cFMinuto.setSelectedItem(minuto(ev.gethSalida()));
    }
    
    private String hora(LocalTime h){
        return h.toString().substring(0, 2);
    }
    
    private String minuto(LocalTime h){
        return h.toString().substring(3, 5);
    }
    
