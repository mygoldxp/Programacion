    private void mostrar(){
        
        contador = contador.substring(1, contador.length());
        
        if(contador.length()==0){
            contador = "7";
        }
        else if(contador.substring(0,1).equals("7") && contador.length() > 1){
            contador = contador.substring(1, contador.length());
        }
        
        int numero = Integer.parseInt(contador.substring(0,1));
        
        switch(numero){
            case 1:
                jpHijo1.setVisible(true);
                break;
            case 2:
                jpHijo2.setVisible(true);
                break;
            case 3:
                jpHijo3.setVisible(true);
                break;
            case 4:
                jpHijo4.setVisible(true);
                break;
            case 5:
                jpHijo5.setVisible(true);
                break;
            case 6:
                jpHijo6.setVisible(true);
                break;
            default: 
                JOptionPane.showMessageDialog(this, "No se puede añadir más Hij@s, el límite de agregado es de 6 hij@s.");
                bAñadir.setVisible(false);
                break;
        }
    }
    
