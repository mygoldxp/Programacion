    private void validarHijo(JTextField nombre, JCalendarCombo fecha, JButton boton) throws Exception{
        try{
            validar(nombre, "^[A-Z][a-z]{2,}([ ][A-Z][a-z]{1,})*$",1);
            validarFecha(fecha);
            aceptado(nombre, fecha);
            boton.setEnabled(true);
            mostrar();
        }
        catch(Error e){
            JOptionPane.showMessageDialog(this, "El Campo en Rojo es Obligatorio y tiene un formato: " + e.getMessage());
        }
    }
    
    private void agregarHijo(){
        java.text.SimpleDateFormat ValFecha = new java.text.SimpleDateFormat("yyyy-MM-dd");
        for(int x =1; x<(7) ;x++){
            if(!contador.contains(String.valueOf(x))){
                switch(x){
                    case 1:
                        Main.agregarH(tfNombreHijo1.getText(), ValFecha.format(jCalendarCombo1.getDate()));
                        break;
                    case 2:
                        Main.agregarH(tfNombreHijo2.getText(), ValFecha.format(jCalendarCombo2.getDate()));
                        break;
                    case 3:
                        Main.agregarH(tfNombreHijo3.getText(), ValFecha.format(jCalendarCombo3.getDate()));
                        break;
                    case 4:
                        Main.agregarH(tfNombreHijo4.getText(), ValFecha.format(jCalendarCombo4.getDate()));
                        break;
                    case 5:
                        Main.agregarH(tfNombreHijo5.getText(), ValFecha.format(jCalendarCombo5.getDate()));
                        break;
                    case 6:
                        Main.agregarH(tfNombreHijo6.getText(), ValFecha.format(jCalendarCombo6.getDate()));
                        break;
                }
            }
        }
    }
    
