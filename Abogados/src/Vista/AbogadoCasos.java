    private void bAnnadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAnnadirActionPerformed
        // TODO add your handling code here:
        try{
            Casos caso = null;
            caso = Main.consultarCaso(tCaso.getText());
            if(caso == null){
                tCaso.grabFocus();
                throw new Error(13);
            }
            else{
                abo.getCasosCollection().add(caso);
                cargarLista();
                tCaso.setText("");
            }
        } catch (Error e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        } catch (Exception ex) {
            Logger.getLogger(AbogadoCasos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_bAnnadirActionPerformed

    private void bEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEliminarActionPerformed
        // TODO add your handling code here:
        try{
            Casos caso = null;
            caso = Main.consultarCaso(tCaso.getText());
            if(caso == null){
                tCaso.grabFocus();
                throw new Error(13);
            }
            else{
                boolean existe = true;
                for(int x=0; x<tTablaCasos.getRowCount() && existe; x++){
                    if(tCaso.getText().equals(tTablaCasos.getValueAt(x, 0))){
                        abo.getCasosCollection().remove(caso);
                        cargarLista();
                        tCaso.setText("");
                        existe = false;
                    }
                }
                if(existe){
                    throw new Error(14);
                }
            }
        } catch (Error e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        } catch (Exception ex) {
            Logger.getLogger(AbogadoCasos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_bEliminarActionPerformed

