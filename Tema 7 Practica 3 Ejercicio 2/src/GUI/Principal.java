    private void validar(javax.swing.JTextField campo, String condicion, int error) throws Exception{
        Pattern patron = Pattern.compile(condicion);
        Matcher mat = patron.matcher(campo.getText());
        if(!mat.matches()){
            campo.grabFocus();
            campo.setBackground(Color.red);
            throw new Error(error);
        }
        else{
            campo.setBackground(Color.white);
        }
    }
    
    private void validarFecha(JCalendarCombo calendario) throws Exception{
        java.util.Date fecha = new java.util.Date();
        java.text.SimpleDateFormat ValFecha = new java.text.SimpleDateFormat("yyyyMMdd");
        int fe = Integer.valueOf(ValFecha.format(calendario.getDate()));
        int fs = Integer.valueOf(ValFecha.format(fecha));
        if(fe>fs){
            calendario.grabFocus();
            calendario.setBackground(Color.red);
            throw new Error(5);
        }
        else{
            calendario.setBackground(Color.white);
        }
    }
    
