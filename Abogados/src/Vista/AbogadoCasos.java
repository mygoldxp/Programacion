    private void cargarDato(){
        tNombre.setText(abo.getNombre());
        tApe1.setText(abo.getApe1());
        tApe2.setText(abo.getApe2());
        tDir.setText(abo.getDir());
    }
    
    private void cargarLista() throws Exception{
        if(abo.getCasosCollection() != null){
            model.setRowCount(0);
            for(Casos caso : abo.getCasosCollection()){
                Object[] fila = {caso.getNumExp(), caso.getFechaI(), caso.getFechaF(), caso.getEstado(), caso.getClientedni().getDni()};
                model.addRow(fila);
            }
        }
    }
    
    private void habilitar(){
            tCaso.setEnabled(true);
            bEliminar.setEnabled(true);
            bAnnadir.setEnabled(true);
    }
    
    private void bAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAceptarActionPerformed
        // TODO add your handling code here:
        try{
            if(bAceptar.getText().equals("Consultar")){
                abo = Main.consultarAbogado(tDNI.getText());
                if(abo != null){
                    tDNI.setEnabled(false);
                    cargarDato();
                    cargarLista();
                    if(n == 2){
                        bAceptar.setText("Eliminar");
                    }
                    else{
                        habilitar();
                        bAceptar.setText("Aceptar");
                    }
                }
                else{
                    throw new Error(12);
                }
            }
            else{
                siguiente();
            }
        }
        catch(Error e){
            JOptionPane.showMessageDialog(this, e);
        } catch (Exception ex) {
            Logger.getLogger(AbogadoCasos.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_bAceptarActionPerformed

    public void siguiente() throws Exception{
         if(bAceptar.getText().equals("Aceptar")){
            abo.setNombre(tNombre.getText());
            abo.setApe1(tApe1.getText());
            abo.setApe2(tApe2.getText());
            abo.setDir(tDir.getText());
            Main.setAbo(abo);
            Main.modificarAbogado();
            JOptionPane.showMessageDialog(this, "Abodago Modificado Correctamente.");
            Main.cerrar(this);
        }
        else{
            Main.eliminarAbogado(abo.getDni());
            JOptionPane.showMessageDialog(this, "Abogado eliminado con sus Casos Correctamente.");
            Main.cerrar(this);
        }
    }
