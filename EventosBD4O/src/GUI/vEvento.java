    private void adaptaciones(int n){
        setModal(true);
        setLocationRelativeTo(null);
        pEmpresa.setVisible(false);
        pPersona.setVisible(false);
        this.setSize(555, 225);
        
        for(int x = 0; x<25; x++){
            String y = String.valueOf(x);
            if(x<10){
                y = "0" + x;
            }
            cIHora.addItem(String.valueOf(y));
            cFHora.addItem(String.valueOf(y));
        }
        
        for(int x = 0; x<60; x++){
            String y = String.valueOf(x);
            if(x<10){
                y = "0" + x;
            }
            cIMinuto.addItem(String.valueOf(y));
            cFMinuto.addItem(String.valueOf(y));
        }
                
        switch(n){
            case 1:
                this.setTitle("Alta Evento");
                bBuscar.setEnabled(false);
                jListar.setEnabled(false);
                mostrarBAlta();
                bAceptar.setEnabled(true);
                break;
                
            case 2:
                this.setTitle("Modificar Evento");
                bAceptar.setText("Modificar");
                break;
                
            case 3:
                this.setTitle("Eliminar Evento");
                bAceptar.setText("Eliminar");
                break;
                
            case 4:
                this.setTitle("Confirmar Asistencia");
                bAceptar.setText("Confirmar");
                break;
        }
        
        setVisible(true);
    }

