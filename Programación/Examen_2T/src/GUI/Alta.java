    private void borrar(){
        rActor.setSelected(false);
        rDirector.setSelected(false);
        rGuinista.setSelected(false);
        tNombre.setText("");
        tFecha.setText("");
    }
    private void borrarTodo(){
        tTitulo.setText("");
        cGenero.setSelectedIndex(-1);
        rActor.setSelected(false);
        rDirector.setSelected(false);
        rGuinista.setSelected(false);
        tNombre.setText("");
        tFecha.setText("");
    }
    private void validar(int cod, String dato, String patron, JTextField campo) throws Exception{
        Pattern p = Pattern.compile(patron);
        Matcher m = p.matcher(dato);
        if(m.matches()){
            campo.setBackground(Color.white);
        }
        else{
            campo.setBackground(Color.red);
            campo.grabFocus();
            throw new Error(cod);
        }
    }
 
